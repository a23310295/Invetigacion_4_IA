

#Metodologías compatibles
#Si el proyecto es muy grande o trabajan en equipos, CRISP-DM se puede complementar o sustituir por las siguientes metodologías:

#1. Ágil / Scrum (Adaptado para IA)
#Scrum es excelente para la gestión del tiempo y del equipo, pero debe adaptarse. En IA, no puedes prometer un "software funcional" cada dos semanas, pero sí puedes entregar "conocimiento iterativo" (ej. Sprint 1: Limpieza de datos terminada; Sprint 2: Primer modelo base; Sprint 3: Mejora de precisión).

#Compatibilidad: Se usa como un "envoltorio" para CRISP-DM. CRISP-DM guía la ciencia, y Scrum guía al equipo.

#2. KDD (Knowledge Discovery in Databases)
#Es una metodología más antigua y puramente académica. Es ideal si el enfoque de la materia es estrictamente de investigación teórica y extracción de patrones ocultos, más que en crear un producto de software.

#Compatibilidad: Muy alta para proyectos donde el objetivo final es un paper de investigación o una tesis en lugar de una aplicación funcional.

#3. TDSP (Team Data Science Process)
#Creada por Microsoft, es una versión moderna de CRISP-DM que ya integra principios Ágiles desde su concepción. Pone mucho énfasis en el control de versiones (Git), el uso de repositorios compartidos y la documentación estandarizada.

#Compatibilidad: Excelente si los alumnos ya tienen bases fuertes de ingeniería de software y están usando herramientas en la nube.

#4. Design Thinking
#No sirve para codificar la IA, pero es fantástica para las primeras semanas del semestre. Ayuda a los estudiantes a investigar y empatizar con un problema real antes de decidir qué tipo de Inteligencia Artificial van a construir.

#Compatibilidad: Se usa en la "Fase 0" del proyecto para definir la idea.
